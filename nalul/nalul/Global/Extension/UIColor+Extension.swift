import UIKit

extension UIColor {
    
    @nonobjc class var nalulBlack: UIColor {
        return UIColor(red: 20.0 / 255.0, green: 20.0 / 255.0, blue: 20.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var nalulDarkGray: UIColor {
        return UIColor(red: 53.0 / 255.0, green: 51.0 / 255.0, blue: 51.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var nalulLightGray: UIColor {
        return UIColor(red: 96.0 / 255.0, green: 93.0 / 255.0, blue: 93.0 / 255.0, alpha: 1.0)
    }

}
